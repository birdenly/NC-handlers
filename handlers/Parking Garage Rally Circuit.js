Game.Play = function() {

  Context.StartArguments = " -w -t --resolution " + Context.Width + "x" + Context.Height + " --position " + Context.PosX + "," +Context.PosY;

  var steamPath = Context.GetFolder(Nucleus.Folder.InstancedGameFolder) + "\\steam_api64.dll";
  var savePkgOrigin = System.IO.Path.Combine(Game.Folder, "steam_api64.dll");
  System.IO.File.Copy(savePkgOrigin, steamPath, true);

  var savePath = Context.EnvironmentPlayer + Context.UserProfileConfigPath + "\\settings.cfg";
  if (!System.IO.File.Exists(savePath)){

    var savePkgOrigin = System.IO.Path.Combine(Game.Folder, "settings.cfg");
    System.IO.File.Copy(savePkgOrigin, savePath, true);
  }

  var dict = [
  Context.FindLineNumberInTextFile(savePath, "visuals_window_style", Nucleus.SearchType.StartsWith) + '|visuals_window_style=1'
  ];
  Context.ReplaceLinesInTextFile(savePath, dict);


  Context.PatchFile(
    System.IO.Path.Combine(Context.RootFolder, "garagerally.exe"),
    System.IO.Path.Combine(Context.RootFolder, "garagerally.exe"),
    [0x39, 0x8e, 0xe3, 0x3f],
    Context.ConvertToBytes(Context.AspectRatioDecimal)
  );

  var numPlayers = 0;
  for (var i = 0; i < PlayerList.Count; i++) {
    var player = PlayerList[i];

    if (player.IsXInput && player.ScreenIndex !== -1) {
      numPlayers++;
    }
    player.ProtoController1 = Context.GamepadId;
    player.ProtoController2 = Context.GamepadId;
    player.ProtoController3 = Context.GamepadId;
    player.ProtoController4 = Context.GamepadId;
    player.ProtoController5 = Context.GamepadId;
    player.ProtoController6 = Context.GamepadId;
    player.ProtoController7 = Context.GamepadId;
    player.ProtoController8 = Context.GamepadId;
    player.ProtoController9 = Context.GamepadId;
  }
};
