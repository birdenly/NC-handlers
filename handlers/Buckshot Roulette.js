Game.Play = function() {
  var vulkan = Context.Options["Vulkan"];

  if (vulkan == "Yes") {
    Context.StartArguments = "--rendering-driver opengl3 -w -t --resolution " + Context.Width + "x" + Context.Height + " --position " + Context.PosX + "," +Context.PosY;
  }
  if (vulkan == "No") {
    Context.StartArguments = " -w -t --resolution " + Context.Width + "x" + Context.Height + " --position " + Context.PosX + "," +Context.PosY;
  }

  var steamPath = Context.GetFolder(Nucleus.Folder.InstancedGameFolder) + "\\steam_api64.dll";
  var savePkgOrigin = System.IO.Path.Combine(Game.Folder, "steam_api64.dll");
  System.IO.File.Copy(savePkgOrigin, steamPath, true);

};
